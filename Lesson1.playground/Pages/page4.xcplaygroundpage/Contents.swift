/*:
 # データ型を調べる
 */
/*:
 文字列のオブジェクト `alphabet` がどんなデータ型かを調べる
 
 ````
 var alphabet = "abc"
 ````
 */
var alphabet = "abc"




/*:
 データ型を調べるには `type(of:)` 機能を使う
 
 ````
 type(of: alphabet)
 ````
 文字列を扱えるオブジェクトのデータ型は `String` になる。
 */
type(of: alphabet)



/*:
 ## 整数のデータ型
 整数オブジェクト `number` のデータ型を調べると

 ````
 var number = 123
 type(of: number)
 ````
 整数を扱うオブジェクトの型は `Int` とわかった
 */
var number = 123
type(of: number)






