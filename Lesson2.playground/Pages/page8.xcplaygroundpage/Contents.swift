var bloodType = "A"

if bloodType == "A" {
    print("Patient, Responsible, Sensitive.")   // 我慢, 責任感, 繊細
} else if bloodType == "B" {
    print("Active, Creative, Passionate.")  // 積極的, 想像的, 情熱
} else if bloodType == "O" {
    print("Confident, Competitive, Unpredictable.")    // 自信家, 競争心, 気まぐれ
} else {
    print("Aociable, Adaptable, Innocent.") // 社交的, 順応性, 純朴
}




