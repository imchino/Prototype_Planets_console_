/*:
 呼び出す機能のことを関数という
 関数の定義と呼び出しを理解する
 funcキーワード、メソッド名、パラメータリストなどのメソッド実装の構文を理解する
 */
/*:
 コンソールに文字列を出力するにはprint()機能を呼び出す
 funcキーワードを使うと、独自の機能を定義できる
 挨拶する機能 sayHello() を定義するには...
 */
func sayHello() {
    print("Hello.")
}



/*:
 定義した機能は同じファイル内であれば、いつでも呼び出せる
 */
sayHello()




/*:
 呼び出して使える機能のことを関数という
 ()も含めて、関数の名前
